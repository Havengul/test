#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i])
		i++;
	s1[i + n] = '\0';
	while (n > 0)
	{
		n--;
		s1[i + n] = s2[n];
	}
	return (s1);
}
